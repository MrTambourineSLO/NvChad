---@type MappingsTable
local M = {}

M.general = {
  n = {
    [";"] = { ":", "enter command mode", opts = { nowait = true } },

    --  format with conform
    ["<leader>fm"] = {
      function()
        require("conform").format()
      end,
      "formatting",
    }

  },
  v = {
    [">"] = { ">gv", "indent"},
  },
}

-- more keybinds!

M.dap = {
  plugin = true,
  n = {
    ["<leader>db"] = {
      "<cmd> DapToggleBreakpoint <CR>",
      "Add breakpoint at line",
    },
    ["<leader>dr"] = {
      "<cmd> DapContinue <CR>",
      "Start or continue the debugger",
    }
  }
}
M.cmake = {
  plugin = true,
  n = {
   ["<leader>cg"] = {
      "<cmd> CMakeGenerate <CR>",
      "Generate CMAKE file"
    },
    ["<leader>cb"] = {
      "<cmd> CMakeBuild <CR>",
      "Build CMAKE file"
    }
  }
}
return M
